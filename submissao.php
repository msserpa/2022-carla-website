<h2>Submissão</h2>

<p>
Os artigos devem ser escritos em <b>inglês</b>, obedecendo as datas
estipuladas no item <a href="datas.php">Datas Importantes</a>. Cada
artigo deve conter entre <b>2 a 4 páginas</b>.
</p>

<p>
O WSPPD recomenda a submissão de artigos completos, porém alunos de mestrado e 
doutorado que tenham um artigo publicado neste ano, em seu tópico de pesquisa atual, 
ou possuem artigo submetido ou pronto para a submissão, podem utilizar a opção de envio 
<b>Abstract Only</b>. Este formato foi incluído a partir da edição XV do WSPPD para 
<b>incentivar</b> a <b>colaboração</b> e <b>discussão</b> entre os pesquisadores 
participantes do evento.
</p>

<p>
Pedimos aos autores que usarem a opção Abstract Only que adicionem uma seção de <b>Disclaimer</b>,
informando em qual conferência ou journal o trabalho foi submetido ou publicado. 
</p> 

<p>
O formato LaTeX para submissão está disponível <a
href="downloads/wsppd-paper.tar.gz">neste pacote</a>. A submissão
deverá ser feita em PDF (página A4).
<br>
Também, está disponível neste <a href="https://github.com/schnorr/ieeeorg">link</a>, o formato LaTeX em org, para quem utiliza org-mode.
</p>

<p> 
Todos os autores devem fazer a submissão dos artigos pelo <b>EasyChair</b> através da URL: 
<center>
<a href="https://easychair.org/cfp/wsppd2020">https://easychair.org/cfp/wsppd2020</a>
</center>
Para a submissão será necessária a criação de uma conta nesse sistema.
</p>

<p>Os artigos devem estar em formato PDF no momento da submissão.</p>

<p>Todos os artigos aceitos serão publicados nesta página (anais
eletrônicos) e se possível em outro formato (papel ou mídia).</p>

